#!/usr/bin/env python3
'''
A function that concatenates strings
'''


def concat(str1: str, str2: str) -> str:
    '''
    A function that concatenates strings

    Args:
        str1: String 1 to concatenate
        str2: string 2 to concatentate

    Returns: Concatenation of str1 and str2
    '''
    return str1 + str2
